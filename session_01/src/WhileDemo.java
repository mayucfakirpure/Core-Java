
public class WhileDemo {
	
	public static void main(String[] args) {
		
		int i=1,sum=0;
		
		//while(i<=10)
		while(i!=11)
		{
			sum+=i;
			i++;
		}
		System.out.println("Sum of 1st 10 Natural Nos = "+sum);
	}	
}

